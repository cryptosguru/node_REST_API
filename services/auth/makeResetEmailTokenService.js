const jwtService = require('./jwtService')

const SECRET = require('../../config').token.resetEmail
const expiresIn = require('../../config').token.resetEmailExpiresIn

/**
 * @return {Promise} string
 */
module.exports = userEntity => {
  __typecheck(userEntity, 'Object', true)

  let config = {
    payload: {
      resetEmailToken: true,
      email: userEntity.email
    },

    options: {
      algorithm: 'HS512',
      subject: userEntity.id.toString(),
      expiresIn
    }
  }

  return jwtService.sign(config.payload, SECRET, config.options)
}
